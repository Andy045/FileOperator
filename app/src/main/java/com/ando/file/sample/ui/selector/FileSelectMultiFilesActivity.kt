        results.forEach {
            val uri = it.uri ?: return@forEach
            when (FileType.INSTANCE.typeByUri(uri)) {
                FileType.IMAGE -> {
                    val bitmap = getBitmapFromUri(uri)
                    //ÂéüÂõæ
                    mIvOrigin.setImageBitmap(bitmap)
                    mIvOrigin.setOnClickListener {
                        openFileBySystemChooser(this, uri)
